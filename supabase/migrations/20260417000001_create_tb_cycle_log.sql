    BEGIN
        ALTER TABLE public.tb_cycle_log
            ADD CONSTRAINT tb_cycle_log_work_log_fk
            FOREIGN KEY (work_log_id)
            REFERENCES public.tb_work_log(id)
            ON DELETE SET NULL;
    EXCEPTION WHEN duplicate_object THEN
        NULL;
    END;
END $$;

DROP INDEX IF EXISTS public.idx_tb_cycle_log_identity;

CREATE UNIQUE INDEX IF NOT EXISTS idx_tb_cycle_log_identity
    ON public.tb_cycle_log (
        machine_id,
        start_time,
        end_time,
        source_mode,
        algorithm_version
    );

CREATE INDEX IF NOT EXISTS idx_tb_cycle_log_work_log_time
    ON public.tb_cycle_log (work_log_id, start_time);

CREATE INDEX IF NOT EXISTS idx_tb_cycle_log_end_time_desc
    ON public.tb_cycle_log (end_time DESC);

